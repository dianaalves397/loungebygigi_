export function getTranslations(locale: Locale): Record<string, string> {
  return messages[locale] || messages.pt;
}

export function detectLocale(): Locale {
  if (typeof window === "undefined") return "pt";
  try {
    const saved = localStorage.getItem("lounge_locale") as Locale | null;
    if (saved && messages[saved]) return saved;
    const lang = navigator.language?.split("-")[0].toLowerCase() as Locale;
    if (lang && messages[lang]) return lang;
  } catch {}
  return "pt";
}

export function saveLocale(locale: Locale) {
  try {
    localStorage.setItem("lounge_locale", locale);
  } catch {}
}
